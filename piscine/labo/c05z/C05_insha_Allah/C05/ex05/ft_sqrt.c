int	ft_sqrt(int nb)
{
	int	res;
	int	i;

	i = 1;
	res = 1;
	while (res <= nb && i <= 46340)
	{
		res = i * i;
		if (res == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
