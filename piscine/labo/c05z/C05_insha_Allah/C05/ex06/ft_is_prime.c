int	ft_is_prime(int nb)
{
	int	i;

	i = 1;
	if (nb < 2)
		return (0);
	while (i <= (nb / i))
	{
		if ((nb % i) == 0 && i != 1)
			return (0);
		i++;
	}
	return (1);
}
